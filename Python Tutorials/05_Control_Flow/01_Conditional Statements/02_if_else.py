# 1 example

# Define a variable
x = 10

# Check if x is greater than 5
if x > 5:
    # If condition is true, print a message
    print("x is greater than 5")
else:
    # If condition is false, print a different message
    print("x is not greater than 5")

# Output
# x is not greater than 5

