x = 10
x += 5   # Equivalent to: x = x + 5
print("Addition assignment:", x)

y = 20
y -= 5   # Equivalent to: y = y - 5
print("Subtraction assignment:", y)

z = 10
z *= 2   # Equivalent to: z = z * 2
print("Multiplication assignment:", z)


