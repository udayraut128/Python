# A function in Python is a reusable block of code that performs a specific task. It consists of a function header with a name and optional parameters, followed by a function body where the task is defined. Functions can optionally return a value using the return statement.



# Define a function
def greet(name):
    return "Hello, " + name + "!"

# Call the function
message = greet("Alice")
print(message)

#output
# Hello, Alice!