# Creating a tuple of coordinates
point = (3, 7)

# Accessing elements
print("X coordinate:", point[0])
print("Y coordinate:", point[1])

# Tuple unpacking
x, y = point
print("Unpacked coordinates:", x, y)

# Creating a tuple of mixed data types
person = ("John", 30, "New York")

# Accessing elements
print("Name:", person[0])
print("Age:", person[1])
print("City:", person[2])


#output
# X coordinate: 3
# Y coordinate: 7
# Unpacked coordinates: 3 7
# Name: John
# Age: 30
# City: New York